// Control Flow
let score = 85;

// If-else
if (score >= 90) {
    console.log("Grade: A");
} else if (score >= 80) {
    console.log("Grade: B");
} else if (score >= 70) {
    console.log("Grade: C");
} else {
    console.log("Grade: F");
}

// For loop
console.log("Counting:");
for (let i = 0; i < 5; i++) {
    console.log(i);
}

// While loop
let counter = 0;
while (counter < 3) {
    console.log(`While: ${counter}`);
    counter++;
}