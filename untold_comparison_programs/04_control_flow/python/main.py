# Control Flow
score = 85

# If-else
if score >= 90:
    print("Grade: A")
elif score >= 80:
    print("Grade: B")
elif score >= 70:
    print("Grade: C")
else:
    print("Grade: F")

# For loop
print("Counting:")
for i in range(5):
    print(i)

# While loop
counter = 0
while counter < 3:
    print(f"While: {counter}")
    counter += 1